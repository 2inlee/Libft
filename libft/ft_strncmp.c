#include "libft.h"

int         ft_strncmp(const char *s1, const char *s2, size_t n)
{
    size_t  i;

    if (s1 == 0 && s2 == 0)
		return (0);
	else if (s1 == 0 || s2 == 0)
		return (s1 == 0 ? -1 : 1);
    if (n == 0)
        return (0);
    i = 0;
    while ((s1[i] != '\0') && (s2[i] != '\0') && i < (n - 1))
    {
        if (s1[i] != s2[i])
            return (s1[i] - s2[i]);
        i++;
    }
    return (0);
}