#include "libft.h"

size_t		ft_strlcat(char *dest, const char *src, size_t dstsize)
{
	size_t	i;
	size_t	j;
	size_t	dest_len;
	size_t	src_len;

	dest_len = ft_strlen(dest);
	src_len = ft_strlen(src);
	i = ft_strlen(dest);
	j = 0;
	if (dstsize < dest_len + 1)
		return (dstsize + src_len);
	if (dstsize > dest_len + 1)
	{
		while (src[j] != '\0' && i < dstsize - 1)
			*(dest + i++) = *(src + j++);
	}
	*(dest + i) = '\0';
	return (dest_len + src_len);
}
